	<h1>What is the BRIB Token?</h1>

	<p>BRIB is a community token for the Brisket DAO. The token:</p>

	<ul>
		<li>is used to reward contributions to the DAO.</li>
		<li>
			does not have any uses at the moment, but it could in the future be
			used for:

			<ul>
				<li>access to exclusive content.</li>
				<li>
					voting and influence on the future direction of the DAO.
				</li>
				<li>incentivize, encourage, and support Brisket DAO community members to develop their own new crypto projects.</li>
				<li>positively influence the future direction of crypto projects in general.</li>
			</ul>
		</li>
	</ul>

	<p>

		The Brisket DAO is a <strong>community, not an investment</strong> or
		profit sharing scheme. The token is not used to raise capital or to
		promise any kind of return for holders.
		
	</p>

