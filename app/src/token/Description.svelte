	<h2>Tokenomics</h2>

	<p>BRIB has no max supply currently. Current supply and stats are available on the <a href="/#/token">token page</a>.</p>

	<h3>Inflationary Period (2022-2027)</h3>

	<p>
		
		During the early stages of the community, the token supply will increase
		in order to reward community contributions.
	
	</p>

	<p>

		Contributions are tracked and vetted by community members and rewarded
		retroactively. Community members who stick around and consistently
		contribute will be given power to reward others for their contributions.
		
	</p>
		
	<h3>Stabilization Period (2027+)</h3>

	<p>

		As the community grows and stabilizes in its structure, token emissions
		will decrease.
		
	</p>

	<h3>Eventual Max Supply</h3>

	<p>
		The eventual max supply of BRIB will be determined by the community.
